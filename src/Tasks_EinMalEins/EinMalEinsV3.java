package Tasks_EinMalEins;

public class EinMalEinsV3 {

    public static void main(String[] args) {


        for(int a = 1; a <= 10; a++){
            for(int b = 1; b <= 10; b++)

                System.out.println(a +  " * "  + b + " = " +  (a * b));
        }
        System.out.println();



    }
}
